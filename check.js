//判断图片是否存在  
function CheckImg(imgurl) {
    return new Promise(function(resolve, reject) {
        var ImgObj = new Image(); 
        ImgObj.onload=function(){
           // console.log(ImgObj.width+"==",ImgObj.height+"==");
            resolve("load image success");
        }
        ImgObj.onerror=function(){
           // console.log('error');
            reject('Could not load image at ' + imgurl);
        }
        ImgObj.src = imgurl;
    });
}

//设置图片 动态传入 imgurl。若存在则显示该图片，不存在则显示默认图片
function setImage(imgurl){
    //图片存在执行第一个函数，否则执行第二个函数
    CheckImg(imgurl).then(function(json) {
    figure.insertAdjacentHTML('beforeend','<img id="drug_figure2" style="width:711.36px; height:948.48px;" src='+imgurl+'>');
    }, function(error) {
      console.log("图片"+imgurl+"不存在");
    });
}

